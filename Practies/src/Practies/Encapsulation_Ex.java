package Practies;

public class Encapsulation_Ex {
	private String name;
	private int id;
	
	public String getname() {
	return name;
	}
	public void setname(String name) {
		this.name=name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	

}
